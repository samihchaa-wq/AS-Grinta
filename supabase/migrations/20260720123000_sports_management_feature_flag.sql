-- Optional sports-management module: server-side feature flag foundation.
--
-- This migration is intentionally inert at runtime: the flag is inserted as
-- disabled and no existing application workflow is changed.

create schema if not exists private;

create table if not exists private.app_feature_flags (
  key text primary key,
  enabled boolean not null default false,
  config jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now(),
  updated_by uuid null references public.profiles(id) on delete set null,
  constraint app_feature_flags_key_format
    check (key ~ '^[a-z][a-z0-9_]*$'),
  constraint app_feature_flags_config_object
    check (jsonb_typeof(config) = 'object')
);

comment on table private.app_feature_flags is
  'Server-side feature flags. This schema is not exposed through the Data API.';
comment on column private.app_feature_flags.enabled is
  'Authoritative server value. Flutter caches this value for presentation only.';

create table if not exists private.app_feature_flag_audit (
  id bigint generated by default as identity primary key,
  feature_key text not null references private.app_feature_flags(key)
    on update cascade on delete restrict,
  old_enabled boolean not null,
  new_enabled boolean not null,
  actor_profile_id uuid null references public.profiles(id) on delete set null,
  justification text null,
  created_at timestamptz not null default now(),
  constraint app_feature_flag_audit_real_transition
    check (old_enabled is distinct from new_enabled),
  constraint app_feature_flag_audit_justification_length
    check (justification is null or char_length(justification) <= 500)
);

comment on table private.app_feature_flag_audit is
  'Append-only audit trail for feature-flag state transitions.';

alter table private.app_feature_flags enable row level security;
alter table private.app_feature_flag_audit enable row level security;

revoke all on table private.app_feature_flags
  from public, anon, authenticated;
revoke all on table private.app_feature_flag_audit
  from public, anon, authenticated;
revoke all on sequence private.app_feature_flag_audit_id_seq
  from public, anon, authenticated;

grant select, insert, update on table private.app_feature_flags to service_role;
grant select, insert on table private.app_feature_flag_audit to service_role;
grant usage, select on sequence private.app_feature_flag_audit_id_seq
  to service_role;

