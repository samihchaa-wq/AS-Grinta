create or replace function private.is_feature_enabled(p_key text)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select coalesce(
    (
      select f.enabled
      from private.app_feature_flags f
      where f.key = p_key
    ),
    false
  );
$$;

create or replace function private.get_public_feature_flags()
returns jsonb
language sql
stable
security definer
set search_path = ''
as $$
  select jsonb_build_object(
    'sports_management',
    jsonb_build_object(
      'enabled', f.enabled,
      'config', f.config,
      'updated_at', f.updated_at
    )
  )
  from private.app_feature_flags f
  where f.key = 'sports_management';
$$;

create or replace function private.set_sports_management_enabled(
  p_enabled boolean,
  p_justification text default null
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $function$
declare
  v_old_enabled boolean;
  v_config jsonb;
  v_updated_at timestamptz;
  v_justification text := nullif(btrim(p_justification), '');
begin
  if p_enabled is null then
    raise exception 'Feature flag value is required' using errcode = '22023';
  end if;

  if not private.is_admin() then
    raise exception 'Active administrator role required' using errcode = '42501';
  end if;

  if v_justification is not null and char_length(v_justification) > 500 then
    raise exception 'Justification cannot exceed 500 characters'
      using errcode = '22023';
  end if;

  select f.enabled, f.config
  into v_old_enabled, v_config
  from private.app_feature_flags f
  where f.key = 'sports_management'
  for update;

  if not found then
    raise exception 'Sports-management feature flag is missing'
      using errcode = 'P0002';
  end if;

  if v_old_enabled is distinct from p_enabled then
    update private.app_feature_flags f
    set enabled = p_enabled,
        updated_at = now(),
        updated_by = (select auth.uid())
    where f.key = 'sports_management'
    returning f.updated_at into v_updated_at;

    insert into private.app_feature_flag_audit (
      feature_key,
      old_enabled,
      new_enabled,
      actor_profile_id,
      justification
    )
    values (
      'sports_management',
      v_old_enabled,
      p_enabled,
      (select auth.uid()),
      v_justification
    );
  else
    select f.updated_at
    into v_updated_at
    from private.app_feature_flags f
    where f.key = 'sports_management';
  end if;

  return jsonb_build_object(
    'sports_management',
    jsonb_build_object(
      'enabled', p_enabled,
      'config', v_config,
      'updated_at', v_updated_at,
      'changed', v_old_enabled is distinct from p_enabled
    )
  );
end;
$function$;

