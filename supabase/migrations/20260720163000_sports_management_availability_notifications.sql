create or replace function private.dispatch_sport_notification_event(p_event_id bigint)
returns boolean
language plpgsql
security definer
set search_path = ''
as $function$
declare
  v_event record;
  v_token text;
  v_request_id bigint;
begin
  select event.id, event.kind, event.match_id, event.profile_id
  into v_event
  from public.sport_availability_notification_events event
  where event.id = p_event_id
  for update;

  if not found then
    return false;
  end if;

  if v_event.id is null then
    return false;
  end if;

  select secret.decrypted_secret
  into v_token
  from vault.decrypted_secrets secret
  where secret.name = 'push_internal_token';

  if v_token is null then
    update public.sport_availability_notification_events
    set dispatch_status = 'dispatch_failed'
    where id = p_event_id;
    return false;
  end if;

  select net.http_post(
    url := 'https://ovzijmqrnsgcmryinkfa.supabase.co/functions/v1/send-push',
    body := jsonb_build_object(
      'kind', v_event.kind,
      'match_id', v_event.match_id,
      'profile_ids', jsonb_build_array(v_event.profile_id),
      'notification_event_id', v_event.id
    ),
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'x-push-token', v_token
    ),
    timeout_milliseconds := 10000
  ) into v_request_id;

  update public.sport_availability_notification_events
  set dispatch_status = 'dispatch_requested',
      dispatch_requested_at = now(),
      network_request_id = v_request_id
  where id = p_event_id;

  return true;
exception
  when others then
    update public.sport_availability_notification_events
    set dispatch_status = 'dispatch_failed'
    where id = p_event_id;
    return false;
end;
$function$;

revoke all on function private.dispatch_sport_notification_event(bigint)
  from public, anon, authenticated;

create or replace function private.create_sport_availability_notification(
  p_match_id uuid,
  p_participant_id uuid,
  p_profile_id uuid,
  p_kind text,
  p_source text,
  p_scheduled_for timestamptz,
  p_requested_by uuid,
  p_reason text
)
returns bigint
language plpgsql
security definer
set search_path = ''
as $function$
declare
  v_event_id bigint;
begin
  insert into public.sport_availability_notification_events (
    match_id,
    participant_id,
    profile_id,
    kind,
    source,
    scheduled_for,
    requested_by,
    reason
  ) values (
    p_match_id,
    p_participant_id,
    p_profile_id,
    p_kind,
    p_source,
    p_scheduled_for,
    p_requested_by,
    nullif(trim(p_reason), '')
  )
  on conflict do nothing
  returning id into v_event_id;

  if v_event_id is not null then
    perform private.dispatch_sport_notification_event(v_event_id);
  end if;

  return v_event_id;
end;
$function$;

