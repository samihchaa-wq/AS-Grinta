-- Optional sports-management module: availability opening and reminder notifications.
-- Additive and inert while private.app_feature_flags.sports_management is false.

alter table public.push_delivery_log
  drop constraint if exists push_delivery_log_kind_check;

alter table public.push_delivery_log
  add constraint push_delivery_log_kind_check check (
    kind in (
      'new_match',
      'closing_soon',
      'result_validated',
      'availability_open',
      'availability_j3',
      'availability_j1',
      'availability_manual'
    )
  );

create table public.sport_availability_notification_events (
  id bigint generated by default as identity primary key,
  match_id uuid not null references public.match_sport_workflows(match_id) on delete restrict,
  participant_id uuid not null,
  profile_id uuid not null references public.profiles(id) on delete restrict,
  kind text not null check (
    kind in (
      'availability_open',
      'availability_j3',
      'availability_j1',
      'availability_manual'
    )
  ),
  source text not null check (source in ('automatic', 'manual')),
  scheduled_for timestamptz not null,
  requested_at timestamptz not null default now(),
  requested_by uuid references public.profiles(id) on delete set null,
  reason text check (reason is null or char_length(reason) <= 500),
  dispatch_status text not null default 'pending' check (
    dispatch_status in (
      'pending',
      'dispatch_requested',
      'completed',
      'no_subscription',
      'dispatch_failed'
    )
  ),
  dispatch_requested_at timestamptz,
  delivery_completed_at timestamptz,
  network_request_id bigint,
  attempted_count integer not null default 0 check (attempted_count >= 0),
  sent_count integer not null default 0 check (sent_count >= 0),
  failed_count integer not null default 0 check (failed_count >= 0),
  created_at timestamptz not null default now(),
  foreign key (participant_id, match_id)
    references public.match_sport_participants(id, match_id) on delete restrict,
  check (
    (source = 'manual' and kind = 'availability_manual')
    or (source = 'automatic' and kind <> 'availability_manual')
  )
);

comment on table public.sport_availability_notification_events is
  'Per-player history and outbox for availability opening and reminder pushes.';

create unique index sport_availability_notification_auto_once_idx
  on public.sport_availability_notification_events(participant_id, kind)
  where source = 'automatic';

create index sport_availability_notification_match_timeline_idx
  on public.sport_availability_notification_events(match_id, requested_at desc);

create index sport_availability_notification_profile_timeline_idx
  on public.sport_availability_notification_events(profile_id, requested_at desc);

