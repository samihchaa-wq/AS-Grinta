  to service_role;

create or replace function public.internal_mark_sport_notification_delivery(
  p_event_id bigint,
  p_attempted integer,
  p_sent integer,
  p_failed integer
)
returns boolean
language plpgsql
security definer
set search_path = ''
as $function$
begin
  update public.sport_availability_notification_events
  set attempted_count = greatest(coalesce(p_attempted, 0), 0),
      sent_count = greatest(coalesce(p_sent, 0), 0),
      failed_count = greatest(coalesce(p_failed, 0), 0),
      dispatch_status = case
        when greatest(coalesce(p_attempted, 0), 0) = 0 then 'no_subscription'
        else 'completed'
      end,
      delivery_completed_at = now()
  where id = p_event_id;

  return found;
end;
$function$;

