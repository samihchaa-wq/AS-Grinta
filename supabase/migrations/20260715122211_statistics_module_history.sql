-- Module Statistiques : données historiques importées du fichier validé
-- et vue unifiée avec la saison ouverte.

create table if not exists public.historical_player_statistics (
  id bigint generated by default as identity primary key,
  scope text not null check (scope in ('previous', 'all_time')),
  season_name text,
  display_rank integer not null check (display_rank > 0),
  player_name text not null check (nullif(btrim(player_name), '') is not null),
  is_goalkeeper boolean not null default false,
  matches_played integer not null check (matches_played >= 0),
  wins integer not null check (wins >= 0),
  draws integer not null check (draws >= 0),
  losses integer not null check (losses >= 0),
  goals integer not null default 0 check (goals >= 0),
  hdm integer not null default 0 check (hdm >= 0),
  clean_sheets integer check (clean_sheets is null or clean_sheets >= 0),
  source_label text not null default 'classements_joueurs_et_gardien_corriges',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (scope, player_name),
  check (
    (scope = 'previous' and season_name is not null)
    or (scope = 'all_time' and season_name is null)
  )
);

alter table public.historical_player_statistics enable row level security;

drop policy if exists "Authenticated users can read historical statistics"
  on public.historical_player_statistics;
create policy "Authenticated users can read historical statistics"
  on public.historical_player_statistics
  for select
  to authenticated
  using (true);

revoke all privileges on table public.historical_player_statistics from anon;
revoke insert, update, delete, truncate, references, trigger
  on table public.historical_player_statistics from authenticated;
grant select on table public.historical_player_statistics to authenticated;
grant usage, select on sequence public.historical_player_statistics_id_seq
  to service_role;

