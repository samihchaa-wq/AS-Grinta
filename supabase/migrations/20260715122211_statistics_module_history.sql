create or replace view public.v_statistics_players
with (security_invoker = true)
as
with current_season as (
  select id, name
  from public.seasons
  where status = 'open'
  order by created_at desc
  limit 1
),
current_base as (
  select
    cs.name as period_label,
    sp.position as display_order,
    sp.first_name as player_name,
    sp.is_goalkeeper,
    coalesce(st.goals, 0)::integer as goals,
    coalesce(st.clean_sheets, 0)::integer as clean_sheets,
    case
      when sp.is_goalkeeper then coalesce(st.clean_sheets, 0)
      else coalesce(st.goals, 0)
    end::integer as ranking_metric
  from current_season cs
  join public.season_players sp
    on sp.season_id = cs.id
   and sp.is_active
  left join public.v_player_season_stats st
    on st.season_player_id = sp.id
),
current_ranked as (
  select
    'current'::text as period_key,
    period_label,
    rank() over (
      partition by is_goalkeeper
      order by ranking_metric desc
    )::integer as display_rank,
    coalesce(display_order, 9999)::integer as display_order,
    player_name,
    is_goalkeeper,
    null::integer as matches_played,
    null::integer as wins,
    null::integer as draws,
    null::integer as losses,
    goals,
    null::integer as hdm,
    clean_sheets
  from current_base
),
historical as (
  select
    scope as period_key,
    case
      when scope = 'previous' then season_name
      else 'Toutes saisons terminées'
    end as period_label,
    display_rank,
    display_rank as display_order,
    player_name,
    is_goalkeeper,
    matches_played,
    wins,
    draws,
    losses,
    goals,
    hdm,
    clean_sheets
  from public.historical_player_statistics
)
select * from current_ranked
union all
select * from historical;

